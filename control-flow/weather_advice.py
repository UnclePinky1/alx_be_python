weather = input("What is the weather like today? (sunny/rainy/cold): ")

if weather == "sunny":
    print("Wear a t-shirt and sunglasses. ")
elif  weather == "rainy":
    print("Don't forget your umbrella and a raincoat. ")
elif  weather == "Cold":
    print("Make sure to wear a warm coat and scarf. ")
else:
    print("Sorry, I dont have recommendation for this weather.")